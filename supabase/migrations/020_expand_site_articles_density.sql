# Inadimplência séria: entre o título vencido, o caixa e o que a papelada consegue provar

Toda empresa convive com atraso pontual. O problema crítico começa quando o atraso deixa de ser exceção operacional e vira padrão com impacto de caixa, desgaste de equipe e perda de previsibilidade. Nessa hora, o tema não é apenas “cobrar melhor”; é gerir crise de crédito com método. Sem método, o financeiro vira central de urgência permanente, o comercial perde foco e a diretoria decide sob pressão com informação incompleta.

Inadimplência séria costuma gerar três efeitos simultâneos: compressão de caixa, aumento de custo oculto de cobrança e ruído interno sobre responsabilidades. O erro mais caro é tratar tudo como caso isolado. Quando valores vencidos aumentam em sequência, a empresa precisa responder como sistema, não como improviso.

## 1) Classificar para agir: atraso leve, relevante e estrutural

Nem todo atraso exige a mesma resposta. Uma segmentação prática:

- **Atraso leve:** poucos dias, causa operacional, relação ainda cooperativa.
- **Atraso relevante:** recorrência, impacto na programação financeira, sinais de tensão.
- **Atraso estrutural:** silêncio do devedor, contestação persistente, incapacidade material de pagamento ou processo de insolvência.

Essa classificação evita erro de timing: ser duro cedo demais com quem ainda negocia de boa-fé, ou ser leniente com quem já migrou para padrão de risco elevado.

## 2) Caixa sob estresse: decisões que não podem esperar

Quando a inadimplência cresce, as primeiras 2 a 4 semanas definem a qualidade do trimestre. Prioridades objetivas:

1. Reprojetar fluxo de caixa por cenário (base, estressado, severo).
2. Rever liberação de novos pedidos para sacados com atraso crítico.
3. Rebalancear concentração para reduzir exposição incremental.
4. Definir governança de crise (quem decide, em que prazo, com quais dados).

Sem esse rito, a operação tenta “recuperar no braço” e, ao mesmo tempo, continua acumulando risco novo.

## 3) O que a documentação precisa provar

Em cobrança estruturada e em sinistro, a discussão central quase sempre volta para prova. O conjunto documental mínimo deveria demonstrar:

- origem da obrigação (pedido/contrato e condições comerciais);
- execução da entrega (evidências objetivas de recebimento/aceite);
- valor, vencimento e saldo;
- histórico de comunicação e tentativas de regularização.

Quando essa trilha é frágil, a empresa perde vantagem técnica e passa a discutir premissas básicas em vez de discutir solução financeira.

## 4) Seguro de crédito no cenário de inadimplência

Quando há seguro contratado e o evento está coberto, o processo de sinistro pode mitigar parte do impacto econômico. Para capturar esse valor, três pontos são críticos:

- comunicação dentro do prazo previsto;
- aderência da operação às condições da apólice;
- documentação organizada para instrução célere.

A falsa crença de que “estar segurado basta” normalmente aparece tarde: no momento da exigência documental ou de análise de enquadramento do evento.

## 5) Cobrança: eficiência sem destruir governança

A cobrança eficaz não é só insistência. É estratégia por estágio:

| Estágio | Objetivo | Ação predominante |
|---|---|---|
| Pré-contencioso | Recuperar rápido com menor atrito | negociação estruturada + formalização |
| Contencioso inicial | Preservar direito e elevar pressão técnica | documentação robusta + rito jurídico |
| Recuperação prolongada | Maximizar recuperação incremental | priorização por probabilidade de êxito |

O ponto-chave é manter coerência entre postura comercial e postura de crédito. Mensagens contraditórias (libera pedido novo enquanto cobra atraso antigo) derrubam poder de negociação.

## 6) Modelo operacional recomendado (90 dias)

Um ciclo de estabilização realista:

- **Dias 1-15:** triagem da carteira vencida, classificação por criticidade, definição de responsáveis.
- **Dias 16-45:** execução intensiva por clusters (valor, probabilidade de recuperação, urgência de caixa).
- **Dias 46-90:** revisão de política de limite, gatilhos de bloqueio e integração com cobertura de seguro.

Ao final, a empresa precisa sair com menos estoque vencido e com prevenção melhor para não recriar o mesmo problema no trimestre seguinte.

## 7) Onde a Hirayama agrega valor

Na prática com Coface, a corretora ajuda a conectar as peças: entendimento de cobertura, fluxo de comunicação, organização de expectativa sobre prazo e apoio na interlocução técnica. Isso reduz ruído decisório no momento de maior tensão financeira.

## Conclusão

Inadimplência grave não se resolve com frase motivacional nem com uma única alavanca. O que funciona é arquitetura: classificação de risco, governança de resposta, disciplina documental, cobrança consistente e uso correto da camada securitária quando existente. Empresas que institucionalizam esse ciclo sofrem menos com eventos inevitáveis do crédito comercial e recuperam mais capacidade de planejar.

