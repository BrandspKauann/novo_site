# Carteira a prazo quando o volume cresce e o risco deixa de ser abstrato

Quem vende para empresas convive com um paradoxo conhecido: o prazo comercial ajuda a crescer receita, mas cada dia a mais de recebimento aumenta o tempo em que o caixa fica exposto ao comportamento do sacado. Em fases iniciais, essa tensão parece administrável; quando a carteira cresce e fica concentrada em poucos clientes, ela deixa de ser “sensação de desconforto” e vira tema de governança. A pergunta muda de “vamos fechar este pedido?” para “quanto desta decisão a companhia consegue absorver sem comprometer trimestre, covenants e plano de investimento?”.

Essa virada de maturidade costuma acontecer sem aviso formal. O comercial segue empurrando meta, o financeiro amplia controles de forma reativa, e a diretoria percebe tarde que já opera com risco sistêmico de concentração. Nessa etapa, confiar apenas na memória do time (“esse cliente sempre pagou”) é abrir mão de método. Histórico ajuda, mas não captura ruptura abrupta de liquidez, mudança de estratégia do comprador, disputa operacional sobre entrega ou eventos de mercado que afetam uma cadeia inteira.

## 1) O ponto de inflexão: quando o risco deixa de ser estatística

Sinais clássicos de que a carteira entrou em zona de atenção:

- Mais de 35% do faturamento a prazo concentrado em poucos sacados.
- Aumento da diferença entre “resultado contábil” e “caixa realizado”.
- Decisões relevantes de limite tomadas no calor da meta mensal.
- Reuniões de fechamento com explicações recorrentes para atrasos recorrentes.
- Dependência de renegociação de prazo para sustentar crescimento.

Nenhum desses sinais, isoladamente, “condena” a operação. O problema é a combinação. Quando vários aparecem juntos, o risco deixa de ser probabilidade difusa e passa a ser impacto potencial com data de materialização incerta.

## 2) Onde a análise de crédito ajuda — e onde para

A análise de crédito empresarial é insubstituível para reduzir erro grosseiro. Ela organiza dados, impõe critérios e dá rastreabilidade para auditoria. Empresas mais robustas normalmente combinam:

- fontes externas (bureaus, cadastros e inteligência setorial);
- histórico interno de atraso, contestação e comportamento de compra;
- rating próprio por faixa de risco;
- política escrita de aprovação e alçada por valor.

Isso melhora a qualidade da decisão, mas não transforma incerteza em garantia. A análise decide melhor **antes**; ela não indeniza **depois** quando o evento adverso acontece.

## 3) Limite de crédito como instrumento de estratégia (não só de controle)

Limite não é número “para travar vendas”. É alocação consciente de balanço. Quem trata limite como anexo operacional perde duas vantagens:

1. Priorizar crescimento em clientes com relação risco-retorno melhor.
2. Evitar que um único evento comprometa o plano comercial inteiro.

Um framework simples para discutir limite com qualidade:

| Dimensão | Pergunta de gestão | Decisão recomendada |
|---|---|---|
| Concentração | Quanto dos recebíveis está nos Top 5? | Se alto, reduzir teto individual e criar gatilho de revisão quinzenal |
| Sazonalidade | O setor do sacado oscila em ciclos previsíveis? | Ajustar prazo/limite por janela de risco |
| Histórico | Atraso foi pontual ou padrão recorrente? | Diferenciar exceção de comportamento estrutural |
| Margem | A margem suporta um calote relevante? | Se não, tratar exposição como risco estratégico |

## 4) Seguro de crédito: camada contratual para risco de cauda

No desenho maduro de carteira, o seguro de crédito entra como camada contratual de transferência parcial de risco em eventos cobertos. Não substitui política interna, não elimina necessidade de documentação e não funciona por slogan. Funciona por:

- enquadramento adequado de sacados e limites;
- aderência da operação às regras da apólice;
- comunicação tempestiva em caso de atraso relevante;
- disciplina documental para sustentar sinistro.

É por isso que empresas que mais capturam valor do seguro não são as que “delegam risco”, e sim as que já têm processo mínimo e usam a apólice como amplificador de governança.

## 5) Documentação: o ativo invisível da carteira

Quando a inadimplência escala, a qualidade da documentação define velocidade e qualidade de resposta. O básico que não pode falhar:

- comprovação clara de entrega/aceite;
- vínculo entre pedido, nota e condição comercial;
- histórico de cobrança datado;
- trilha de aprovação de limite e revisões.

Sem esse rastro, a empresa gasta energia provando o que deveria estar pacificado e perde tempo justamente quando o caixa pede decisão rápida.

## 6) Indicadores que deveriam estar no painel do board

Para tirar o tema do campo opinativo, acompanhe pelo menos:

- DSO por cluster de cliente;
- concentração por sacado e por grupo econômico;
- percentual de títulos acima de 30/60/90 dias;
- perda esperada vs perda realizada;
- exposição coberta vs não coberta.

Com esse painel, a conversa sobe de nível: sai “acho que está tranquilo” e entra “esse nível de exposição cabe ou não no nosso apetite de risco?”.

